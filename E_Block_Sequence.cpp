void solve(ll tc_no)
{
    
    ll n; cin >> n;
    vector<int> v(n); inp(v);

    vector<int> dp(n+1);
    dp[n] = 0;

    for(ll i = n-1 ; i>=0 ; i--)
    {
        ll temp = (v[i]+i <= n-1 ? dp[i+1+v[i]] : INT_MAX);
        dp[i] = min(1+dp[i+1], temp);
    }
    
    cout << dp[0] << "\n";
}

